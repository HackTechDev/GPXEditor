	<br />
	<h3><a name="gpxe-HelpTrkDel" class="gpxe-aref">Supprimer une randonnée</a></h3>
	<p>Cliquer au-dessus d'une randonnée pour la supprimer</p>
	<br />
	<h3><a name="gpxe-HelpTrkConv" class="gpxe-aref">Convertir une randonnée en itinéraire</a></h3>
	<p>Cliquer au-dessus d'une randonnée pour la convertir en itinéraire</p>
