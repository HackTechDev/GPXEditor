	<h2><a name="gpxe-HelpNavigation" class="gpxe-aref">Navigation dans la carte</a></h2>
	<br />
	<div style="padding-left: 10px">
		<h3><a name="gpxe-HelpNavigationMouse" class="gpxe-aref">à la souris</a></h3>
		<p>
			Pour vous déplacer dans la carte, cliquer sur celle-ci et déplacer la souris dans le sens du déplacement voulu en maintenant le bouton pressé.
		</p>
		<p>
			Double-cliquer pour centrer et zoomer sur une zone.
		</p>
		<p>
			Utiliser la touche <code>"shift"</code> et sélectionner une zone rectangulaire pour zoomer sur une portion de carte.
		</p>
		<p>
			Utiliser la roulette pour effectuer un zoom avant ou arrière.
		</p>
		<br />
		<h3><a name="gpxe-HelpNavigationKeyboard" class="gpxe-aref">au clavier</a></h3>
		<p>la touche <code>"+"</code> permet d'effectuer un zoom +</p>
		<p>la touche <code>"-"</code> permet d'effectuer un zoom-</p>
		<p>les flèches permettent de déplacer la carte <i>(en utilisant <code>"ctrl + flèche"</code> saut de 75% de la zone affichée)</i></p>
		<p>la touche <code>"a"</code> permet d'afficher tous les éléments GPX</p>
		<p>la combinaison des touche <code>"ctrl + a"</code> permet d'afficher l'intégralité de la carte</p>
		<p>Au-dessus d'un élément, utiliser la touche <code>"e"</code> pour en modifier les informations</p>
	</div>
	
	<br />
	<br />
	<h2><a name="gpxe-HelpWpt" class="gpxe-aref">Les points</a></h2>
	<div style="padding-left: 10px">
	<br />
	<h3><a name="gpxe-HelpWptAdd" class="gpxe-aref">Ajouter un point</a></h3>
	<p>cliquer simplement sur la carte</p>
	<br />
	<h3><a name="gpxe-HelpWptMdf" class="gpxe-aref">Modifier un point</a></h3>
	<br />
	<p>au-dessus d'un point, cliquer puis déplacer la souris en maintenant le bouton enfoncé</p>
	<br />
	<h3><a name="gpxe-HelpWptDel" class="gpxe-aref">Supprimer un point</a></h3>
	<p>cliquer au-dessus du point à supprimer</p>
	</div>

	<br />
	<br />
	<h2><a name="gpxe-HelpRte" class="gpxe-aref">Les itinéraires</a></h2>
	<div style="padding-left: 10px">
	<br />
	<h3><a name="gpxe-HelpRteAdd" class="gpxe-aref">Ajouter/Prolonger un itinéraire</a></h3>
	<p>cliquer sur la carte pour commencer à tracer un itinéraire, ajouter des points en cliquant et finaliser le tracé en double cliquant</p>
	<p><i>(si le premier clic se fait au-dessus d'un itinéraire, vous allez le prolonger)</i></p>
	<br />
	<h3><a name="gpxe-HelpRteMdf" class="gpxe-aref">Modifier/Couper un itinéraire</a></h3>
	<p>cliquer sur l'itinéraire pour passer en mode édition</p>
	<p>utiliser les points de contrôle pour modifier le tracé</p>
	<p>au-dessus d'un point de contrôle, utiliser la touche <code>"d"</code> pour le supprimer</p>
	<p>au-dessus d'un point de contrôle, utiliser la touche <code>"e"</code> pour en modifier l'altitude</p>
	<p>pour couper un itinéraire en deux, utiliser la touche <code>"c"</code> au-dessus d'un point de contrôle</p>
	<br />
	<h3><a name="gpxe-HelpRteSwapPaste" class="gpxe-aref">Inverser/Coller des itinéraires</a></h3>
	<p>Au-dessus d'un itinéraire, l'extrémité de départ est signalée par un point vert. Utiliser la touche <code>"i"</code> pour l'inverser</p>
	<p>Pour coller deux itinéraires en un, cliquer sur le premier et ensuite sur le deuxième</p>
	<br />
	<h3><a name="gpxe-HelpRteDel" class="gpxe-aref">Supprimer un itinéraire</a></h3>
	<p>Cliquer au-dessus d'un itinéraire pour le supprimer</p>
	</div>

	<br />
	<br />
	<h2><a name="gpxe-HelpTrk" class="gpxe-aref">Les randonnées</a></h2>
